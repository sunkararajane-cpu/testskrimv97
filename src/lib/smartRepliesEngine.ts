export const getSmartReplies = (
  message?: string,
  moodId: string = "default",
): string[] => {
  if (!message) return [];
  const text = message.toLowerCase();
  let category = "default";

  if (
    text.includes("?") ||
    text.includes("how") ||
    text.includes("what") ||
    text.includes("when") ||
    text.includes("where")
  ) {
    category = "question";
  } else if (
    text.includes("game") ||
    text.includes("play") ||
    text.includes("challenge") ||
    text.includes("🎮")
  ) {
    category = "game";
  } else if (
    text.includes("hi") ||
    text.includes("hello") ||
    text.includes("hey") ||
    text.includes("namaste") ||
    text.includes("నమస్కారం")
  ) {
    category = "greeting";
  } else if (
    text.includes("vibe") ||
    text.includes("video") ||
    text.includes("watch") ||
    text.includes("see")
  ) {
    category = "vibe";
  } else if (
    text.includes("!") ||
    text.includes("🔥") ||
    text.includes("🎉") ||
    text.includes("amazing") ||
    text.includes("wow")
  ) {
    category = "hype";
  } else if (
    text.includes("food") ||
    text.includes("eat") ||
    text.includes("biryani") ||
    text.includes("🍛")
  ) {
    category = "food";
  } else if (
    text.includes("sad") ||
    text.includes("miss") ||
    text.includes("😢") ||
    text.includes("sorry")
  ) {
    category = "support";
  }

  const moodKey = ["hype", "chill", "love", "savage"].includes(moodId)
    ? moodId
    : "default";

  // Return random 3 from the selected category and mood
  const options =
    (REPLY_SETS as any)[category][moodKey] ||
    (REPLY_SETS as any)[category]["default"];

  // Shuffle array and pick 3
  const shuffled = [...options].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, 3);
};
